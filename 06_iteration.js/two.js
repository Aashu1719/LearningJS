// Simple while loop
let i = 0;
while (i <= 10) {
    // console.log(`Value of i is ${i}`);
    i = i + 2;
}


// Ex :- 2 Array inside 
let myArray = ['flash', 'batmna', 'superman']
let arr = 0
while (arr < myArray.length) {
    // console.log(`Value is ${myArray[arr]}`);
    arr = arr + 1;
}


// Do-while loop
let score = 0;
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);